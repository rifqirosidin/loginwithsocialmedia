package com.example.tugaseai.ajeng;

public class movie {

    public movie (String title, String img, String release){

        this.title = title;
        this.img = title;
        this.release = release;
    }

    public String getTitle() {
        return title;
    }

    public String getImg() {
        return img;
    }

    public String getRelease() {
        return release;
    }

    private String title, img, release;


}
